#include<stdio.h>
#include<math.h>



int main()
{

	//The sqrt() function calculates the square root of a number, whether it's an integer or a decimal.
	//Example sqrt(164) = 12.896248. By default, "%f" displays 6 decimal places.
	printf("%f \n", sqrt(164));

	//The pow() function computes exponentiation: it raises the first argument to the power of the second.
	//Example: pow(13,2) calculates 13 squared (13^2 = 169).
	printf("%f \n", pow(13, 2));

}